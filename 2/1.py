# Императивная парадигма

# Легкость реализации пошаговых вычислений.
# Простой цикл для генерации таблицы умножения.
# Удобство вывода: Использование вложенного цикла
# и форматированной строки для читаемого отображения таблицы.

# Получаем число n от пользователя
n = int(input("Введите число n: "))

# Генерация и вывод таблицы умножения
for i in range(1, n + 1):
    for j in range(1, n + 1):
        print(f"{i} * {j} = {i * j}")
    print()  # Пустая строка для разделения блоков
