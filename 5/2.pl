% Аккумулятивная рекурсия
% Базовый случай: если список пустой, то аккумулятор содержит сумму элементов (она равна 0).
sum_list([], Acc, Acc).

% Рекурсивный случай: добавляем текущий элемент в аккумулятор и продолжаем для хвоста списка.
sum_list([Head | Tail], Acc, Sum) :-
    NewAcc is Acc + Head,         % Увеличиваем аккумулятор на текущий элемент
    sum_list(Tail, NewAcc, Sum).  % Рекурсивно вызываем для хвоста списка с обновлённым аккумулятором
